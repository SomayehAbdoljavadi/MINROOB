//\/\/\/\/\/\/\/\/\/\/\/\/\/\/\show mouse/\/\/\/\/\/\/\/\/\/\/\/\/\/\/\/\/\/\

void mshow()
 {
  struct REGPACK reg;
  reg.r_ax=01;
  intr(0x33,&reg);
  reg.r_ax=04;
  reg.r_cx=1;
  reg.r_dx=1;
  intr(0x33,&reg);
 }

