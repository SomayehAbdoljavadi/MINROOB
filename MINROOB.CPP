void min()
 {
  int s,t,i,rr=r,cc=c;
  for(i=0;i<16;i++)
   for(j=0;j<16;j++)
     m[i][j]=0;
  randomize();
  if(sw==1) tb=10;
   gotoxy(16,44);
   cout<<("      ");
   gotoxy(17,44);
   cout<<(tb);
   for (i=0;i<=tb;i++)
   {
    s=random(rr);
    t=random(cc);
    m[s][t]=-1;

    if (m[s-1][t]!= -1 )  m[s-1][t]++;
    if (m[s-1][t+1]!= -1) m[s-1][t+1]++;
    if (m[s-1][t-1]!= -1) m[s-1][t-1]++;

    if (m[s][t-1]!= -1)   m[s][t-1]++;
    if (m[s][t+1]!= -1)   m[s][t+1]++;

    if (m[s+1][t]!= -1)   m[s+1][t]++;
    if (m[s+1][t+1]!= -1) m[s+1][t+1]++;
    if (m[s+1][t-1]!= -1) m[s+1][t-1]++;
   }
 }

